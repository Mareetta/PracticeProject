package com.PracticeProject.DAO;

public class Person {
	private String name;
	private String id;
	private int salary ;
	private int post;

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	public int getPost() {
		return post;
	}
	public void setPost(int post) {
		this.post = post;
	}



}
